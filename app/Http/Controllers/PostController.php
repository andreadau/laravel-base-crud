     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();
        $postNew = new Post;
        $postNew->title = $data['title'];
        $postNew->description = $data['description'];
        $postNew->save();

        return redirect()->route('posts.create');
    }

